package com.rowell.sifa.pojo.sys;

import java.util.Date;

/**
 * 定位信息类
 * @author pkx
 *
 */
public class SysLocation {

	private String telno;			//定位号码
	private String telnos;			//定位号码,多个号码用,分隔
	private Date locationtime;		//定位时间
	private String type;			//定位类型(粗定位1，北向2)
	private String result;			//定位结果状态
	private String longitude;		//定位结果经度
	private String latitude;		//定位结果纬度
	private String address;			//定位地址
	private String headString;		//定位传输的信息
	private String sTotalString;	//定位返回的信息
	
	public String getTelnos() {
		return telnos;
	}
	public void setTelnos(String telnos) {
		this.telnos = telnos;
	}
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getsTotalString() {
		return sTotalString;
	}
	public void setsTotalString(String sTotalString) {
		this.sTotalString = sTotalString;
	}
	public String getHeadString() {
		return headString;
	}
	public void setHeadString(String headString) {
		this.headString = headString;
	}
	public String getTelno() {
		return telno;
	}
	public void setTelno(String telno) {
		this.telno = telno;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public Date getLocationtime() {
		return locationtime;
	}
	public void setLocationtime(Date locationtime) {
		this.locationtime = locationtime;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	
}
