package com.rowell.sifa.pojo.terminal;

public class Alarm {

	private String type;
	private String subType;
	private String title;
	private String content;
	private String longitude;
	private String latitude;
	private String occurTime;
	private String status;
	private String location;
	private String repeatNum;
	private String lastOccurTime;
	private String ackUserID;
	private String ackTime;
	private String clearUserID;
	private String clearTime;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getSubType() {
		return subType;
	}

	public void setSubType(String subType) {
		this.subType = subType;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	public String getLatitude() {
		return latitude;
	}

	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

	public String getOccurTime() {
		return occurTime;
	}

	public void setOccurTime(String occurTime) {
		this.occurTime = occurTime;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}

	public String getRepeatNum() {
		return repeatNum;
	}

	public void setRepeatNum(String repeatNum) {
		this.repeatNum = repeatNum;
	}

	public String getLastOccurTime() {
		return lastOccurTime;
	}

	public void setLastOccurTime(String lastOccurTime) {
		this.lastOccurTime = lastOccurTime;
	}

	public String getAckUserID() {
		return ackUserID;
	}

	public void setAckUserID(String ackUserID) {
		this.ackUserID = ackUserID;
	}

	public String getAckTime() {
		return ackTime;
	}

	public void setAckTime(String ackTime) {
		this.ackTime = ackTime;
	}

	public String getClearUserID() {
		return clearUserID;
	}

	public void setClearUserID(String clearUserID) {
		this.clearUserID = clearUserID;
	}

	public String getClearTime() {
		return clearTime;
	}

	public void setClearTime(String clearTime) {
		this.clearTime = clearTime;
	}

}