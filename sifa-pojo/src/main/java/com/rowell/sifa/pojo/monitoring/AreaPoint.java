package com.rowell.sifa.pojo.monitoring;

import java.io.Serializable;

import com.rowell.sifa.pojo.base.DataEntity;
/**
 * 电子围栏区域的点
 * @author FWY
 *
 */
public class AreaPoint extends DataEntity<AreaPoint> implements Serializable{

	/**
	 */
	private static final long serialVersionUID = -4535677622836537320L;
	
	private String areaId;//对象电子围墙的id
	private int no;//排序
	private float lon;//经度
	private float lat;//纬度
	public String getAreaId() {
		return areaId;
	}
	public void setAreaId(String areaId) {
		this.areaId = areaId;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public Float getLon() {
		return lon;
	}
	public void setLon(Float lon) {
		this.lon = lon;
	}
	public Float getLat() {
		return lat;
	}
	public void setLat(Float lat) {
		this.lat = lat;
	}

}
